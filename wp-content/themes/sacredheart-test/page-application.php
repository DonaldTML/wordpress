 <h2 class="ttline">APPLICATION PROCESS</h2>
  <p>Thank you for your interest in a position at Sacred Heart College. In order to ensure the process is as smooth as possible, please carefully read and consider the following information when preparing your application.</p>

  <h3>Submitting applications</h2>
  <p>To be considered for positions at the College, applications must be submitted electronically via the College employment page, be received by the nominated closing time and date, and include the following documents:
  <ul>
    <li>A cover letter, specifically addressing the criteria for success where applicable. It is important that you demonstrate clear links between these criteria and your skills, knowledge and qualifications</li>
    <li>A resume</li>
    <li>An application for employment form</li>
  </ul>
  Applications can be addressed to Anna Negro, Principal.
  </p>

  <h3>Referees</h2>
  <p>The contact details of two work related referees must be included with your application. One must be a direct supervisor and one from a current employer. Written references are not required.</p>
  
  <h3>Timeframes for the recruitment process</h3>
  <p>We will usually contact those shortlisted for an interview by phone, and those not shortlisted will receive an email within four weeks of the date of application. Please note that only those shortlisted for interview will receive feedback on their application (if requested) in the event they are unsuccessful.</p>

  <h3>The Interview</h3>
  <p>Interviews are usually 45-50 minutes in duration, and consist of 2-3 panel members. You will be notified at the time of scheduling as to the composition of your interview panel. <br />At the conclusion of your interview, you will be advised as to the expected timeframe for notification of outcomes.</p>

  <h3>Pre-employment Checks</h3>
<p>Successful applicants will be required to verify and submit the following prior to commencement:<br />
<strong>Qualifications</strong> - original or certified copies. <br />
<strong>100 points of ID</strong> – original or certified copies. Human Resources will advise as to the 100 point criteria.<br />
<strong>Eligibility to work in Australia</strong> - presentation of visa information and passports where required.<br />
<strong>VIT</strong> – teachers will need to advise of their VIT number.<br />
<strong>Working with Children Check</strong> – all non teaching staff are required to hold an Employee WWCC. Employment cannot commence until a person has passed an Employee Check.<br />
<strong>Police Check</strong> – all non teaching staff are required to pass a police check (no more than 3 months old).  A new check is required every 5 years. Employment cannot commence until a person has passed a Police Check.</p>

